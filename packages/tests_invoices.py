class InvoiceModelTests(TestCase):
    def setUp(self):
        self.package = make_package()
        self.inquiry = BookingInquiry.objects.create(
            base_package=self.package,
            customer_name='Amina Juma',
            customer_email='amina@example.com',
            customer_phone='0744123456',
        )
        self.booking = make_booking(self.package, inquiry=self.inquiry)

    def test_number_autogenerated_and_sequential(self):
        inv1 = Invoice.objects.create(booking=self.booking, invoice_type='deposit', amount=Decimal('600'))
        inv2 = Invoice.objects.create(booking=self.booking, invoice_type='balance', amount=Decimal('1800'))
        self.assertTrue(inv1.invoice_number.startswith('INV-'))
        self.assertNotEqual(inv1.invoice_number, inv2.invoice_number)
        # Sequence increments
        seq1 = int(inv1.invoice_number.split('-')[-1])
        seq2 = int(inv2.invoice_number.split('-')[-1])
        self.assertEqual(seq2, seq1 + 1)

    def test_snapshots_customer_and_package(self):
        inv = Invoice.objects.create(booking=self.booking, invoice_type='deposit', amount=Decimal('600'))
        self.assertEqual(inv.customer_name, 'Amina Juma')
        self.assertEqual(inv.customer_email, 'amina@example.com')
        self.assertEqual(inv.package_name, self.package.name)

    def test_snapshot_survives_inquiry_edit(self):
        inv = Invoice.objects.create(booking=self.booking, invoice_type='deposit', amount=Decimal('600'))
        self.inquiry.customer_name = 'Changed Name'
        self.inquiry.save()
        inv.refresh_from_db()
        self.assertEqual(inv.customer_name, 'Amina Juma')

    def test_issued_at_set_on_create(self):
        inv = Invoice.objects.create(booking=self.booking, invoice_type='full', amount=Decimal('2400'))
        self.assertIsNotNone(inv.issued_at)

    def test_booking_protected_from_delete(self):
        Invoice.objects.create(booking=self.booking, invoice_type='deposit', amount=Decimal('600'))
        from django.db.models import ProtectedError
        with self.assertRaises(ProtectedError):
            self.booking.delete()


