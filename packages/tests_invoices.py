class InvoiceViewTests(TestCase):
    def setUp(self):
        cache.delete(SiteSettings.CACHE_KEY)
        self.client = Client()
        self.staff = User.objects.create_user(username='invstaff', password='pass', is_staff=True)
        self.regular = User.objects.create_user(username='invcust', password='pass', is_staff=False)
        self.package = make_package()
        self.inquiry = BookingInquiry.objects.create(
            base_package=self.package,
            customer_name='Amina Juma',
            customer_email='amina@example.com',
            customer_phone='0744123456',
        )
        self.booking = make_booking(self.package, inquiry=self.inquiry)

    def tearDown(self):
        cache.delete(SiteSettings.CACHE_KEY)

    def test_create_requires_staff(self):
        url = reverse('packages:dashboard_invoice_create', args=[self.booking.pk])
        # anonymous
        resp = self.client.post(url, {'invoice_type': 'deposit', 'amount': '600', 'currency': 'USD'})
        self.assertIn(resp.status_code, [301, 302])
        self.assertEqual(Invoice.objects.count(), 0)
        # non-staff
        self.client.login(username='invcust', password='pass')
        resp = self.client.post(url, {'invoice_type': 'deposit', 'amount': '600', 'currency': 'USD'})
        self.assertIn(resp.status_code, [301, 302])
        self.assertEqual(Invoice.objects.count(), 0)

    def test_staff_creates_invoice(self):
        self.client.login(username='invstaff', password='pass')
        url = reverse('packages:dashboard_invoice_create', args=[self.booking.pk])
        resp = self.client.post(
            url, {'invoice_type': 'deposit', 'amount': '600', 'currency': 'USD'}
        )
        self.assertEqual(resp.status_code, 302)
        self.assertEqual(Invoice.objects.count(), 1)
        inv = Invoice.objects.first()
        self.assertEqual(inv.issued_by, self.staff)
        self.assertEqual(inv.customer_name, 'Amina Juma')

    def test_invalid_amount_rejected(self):
        self.client.login(username='invstaff', password='pass')
        url = reverse('packages:dashboard_invoice_create', args=[self.booking.pk])
        resp = self.client.post(
            url, {'invoice_type': 'deposit', 'amount': '0', 'currency': 'USD'}
        )
        self.assertEqual(resp.status_code, 302)
        self.assertEqual(Invoice.objects.count(), 0)

    def test_pdf_endpoint_returns_pdf(self):
        self.client.login(username='invstaff', password='pass')
        inv = Invoice.objects.create(booking=self.booking, invoice_type='deposit', amount=Decimal('600'))
        resp = self.client.get(reverse('packages:dashboard_invoice_pdf', args=[inv.pk]))
        self.assertEqual(resp.status_code, 200)
        self.assertEqual(resp['Content-Type'], 'application/pdf')
        self.assertTrue(resp.content.startswith(b'%PDF-'))

    def test_pdf_endpoint_requires_staff(self):
        inv = Invoice.objects.create(booking=self.booking, invoice_type='deposit', amount=Decimal('600'))
        resp = self.client.get(reverse('packages:dashboard_invoice_pdf', args=[inv.pk]))
        self.assertIn(resp.status_code, [301, 302])

    def test_email_endpoint_sends_with_attachment(self):
        self.client.login(username='invstaff', password='pass')
        inv = Invoice.objects.create(booking=self.booking, invoice_type='deposit', amount=Decimal('600'))
        resp = self.client.post(reverse('packages:dashboard_invoice_email', args=[inv.pk]))
        self.assertEqual(resp.status_code, 302)
        self.assertEqual(len(mail.outbox), 1)
        sent = mail.outbox[0]
        self.assertIn('amina@example.com', sent.to)
        self.assertEqual(len(sent.attachments), 1)
        filename, content, mimetype = sent.attachments[0]
        self.assertTrue(filename.endswith('.pdf'))
        self.assertEqual(mimetype, 'application/pdf')

    def test_booking_detail_shows_invoice_card_and_form(self):
        self.client.login(username='invstaff', password='pass')
        resp = self.client.get(reverse('packages:dashboard_booking_detail', args=[self.booking.pk]))
        self.assertEqual(resp.status_code, 200)
        self.assertContains(resp, 'Issue invoice')


